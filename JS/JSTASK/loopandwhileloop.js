// Using for loop
console.log("Using for loop:");
for (let i = 1; i <= 10; i++) {
    console.log(i);
}

// Using while loop
console.log("Using while loop:");
let j = 1;
while (j <= 10) {
    console.log(j);
    j++;
}

// Using do...while loop
console.log("Using do...while loop:");
let k = 1;
do {
    console.log(k);
    k++;
} while (k <= 10);