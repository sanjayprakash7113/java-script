// Using for loop
console.log("Using for loop:");
for (let i = 10; i >= 1; i--) {
    console.log(i);
}

// Using while loop
console.log("Using while loop:");
let j = 10;
while (j >= 1) {
    console.log(j);
    j--;
}

// Using do...while loop
console.log("Using do...while loop:");
let k = 10;
do {
    console.log(k);
    k--;
} while (k >= 1);